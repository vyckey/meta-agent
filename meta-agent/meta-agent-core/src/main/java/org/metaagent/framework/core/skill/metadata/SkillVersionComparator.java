package org.metaagent.framework.core.skill.metadata;

import java.util.Comparator;

/**
 * Skill version comparator.
 *
 * @author vyckey
 */
public class SkillVersionComparator implements Comparator<String> {
    public static final SkillVersionComparator INSTANCE = new SkillVersionComparator();

    private SkillVersionComparator() {
    }

    @Override
    public int compare(String v1, String v2) {
        return compareVersions(v1, v2);
    }

    private int compareVersions(String v1, String v2) {
        String[] parts1 = v1.split("\\.");
        String[] parts2 = v2.split("\\.");

        int length = Math.max(parts1.length, parts2.length);
        for (int i = 0; i < length; i++) {
            int num1 = i < parts1.length ? Integer.parseInt(parts1[i]) : 0;
            int num2 = i < parts2.length ? Integer.parseInt(parts2[i]) : 0;
            if (num1 != num2) {
                return Integer.compare(num1, num2);
            }
        }
        return 0;
    }
}
